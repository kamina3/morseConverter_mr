    def ja_morse(str)
        i = 0
        conv = ""
        while i < str.length
            morse = j_to_m(str.slice(i))
            conv += morse+"　"
            i+=1
        end
        
        return conv.slice(0, conv.length-1)
    end
    
    private
    def j_to_m(str)
        i=0
        while i < @kana.length
            if(str == @kana[i])
                return @morse[i]
            end
            i += 1
        end
        return str
    end

